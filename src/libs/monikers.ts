const hash = (value: string) => {
  let result = 0;

  for (const character of value) {
    result = Math.imul(31, result) + character.charCodeAt(0);
  }

  return result >>> 0;
};

export const getMoniker = (user: string) => {
  const value = hash(user);
  return `${adjectives[value % adjectives.length]} ${nouns[Math.floor(value / adjectives.length) % nouns.length]}`;
};
